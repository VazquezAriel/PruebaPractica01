    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        if (jTextFieldCedula.getText().trim().equals("") || jTextFieldNombre.getText().trim().equals("") || jTextFieldApellido.getText().trim().equals("")
                || jTextFieldDireccion.getText().trim().equals("") || jTextFieldContraseña.getText().trim().equals("") || jTextFieldFUsuario.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Porfavor rellene todos los campos");

        } else {
            Juez juez = new Juez(jTextFieldFUsuario.getText(), jTextFieldContraseña.getText(), WIDTH, jTextFieldNombre.getText(), jTextFieldApellido.getText()
                    , jTextFieldCedula.getText(), jTextFieldDireccion.getText(), jSpinnerGenero.getValue().toString(), LocalDate.MIN, jSpinnerEstadoCivil.getValue().toString());
            
            if (controladorJuez.crear(juez)) {
                JOptionPane.showMessageDialog(this, "agregado con exito");
                limpiar();

            } else {
                JOptionPane.showMessageDialog(this, "El juez no se guardo porfavor vuelva a intentarlo");

            }

        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

